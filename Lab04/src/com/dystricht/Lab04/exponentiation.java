package com.dystricht.Lab04;

/**
 * Returns function x^m
 * @author dillon strichman
 *
 */
public class exponentiation {
	
	public static double exponentiate(double x, double m){
		
		if(m == 0) return 1;
		
		double result = 0;
		
		result = x * exponentiate(x, m-1);
		
		return result;
		
	}

}
